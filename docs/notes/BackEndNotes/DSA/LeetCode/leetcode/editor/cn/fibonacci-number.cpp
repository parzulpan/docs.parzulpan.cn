class Solution4 {
public:
    int fib(int N) {
        if(N < 1) return 0;
        if(N == 1 || N == 2) return 1;

        int pre = 1, cur = 1;
        for(int i = 3; i <= N; ++i) {
            int sum = pre + cur;
            pre = cur;
            cur = sum;
        }

        return cur;
    }

};

