class Solution1 {
public:
    int fib(int N) {
       if(N < 1) return 0;

       // å¤‡å¿˜å½•å…¨åˆå§‹åŒ–ä¸º0
       vector<int> memo(N + 1, 0);

       return helper(memo, N);
    }

    int helper(vector<int>& memo, int N) {
        // base case
        if(N == 1 || N == 2) return 1;

        // å·²ç»è®¡ç®—è¿‡
        if(memo[N] != 0) return memo[N];

        memo[N] = helper(memo, N - 1) + helper(memo, N - 2);

        return memo[N];
    }
};

// è§£æ³•3ï¼šdpå‘é‡çš„è¿­ä»£è§£æ³•ã€‚å°†å¤‡å¿˜å½•ç‹¬ç«‹å‡ºæ¥ä¸ºä¸€å¼ DPè¡¨ï¼Œåˆ©ç”¨è¿™å¼ è¡¨è‡ªåº•å‘ä¸Šæ±‚è§£ã€‚
class Solution3 {
public:
    int fib(int N) {
        if(N < 1) return 0;
        if(N == 1 || N == 2) return 1;

        // DPè¡¨
        vector<int> dp(N + 1, 0);

        // base case
        dp[1] = dp[2] = 1;
        for(int i = 3; i <= N; ++i) {
            dp[i] = dp[i - 1] + dp[i - 2];
        }

        return dp[N];
    }

};

